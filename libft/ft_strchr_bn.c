#include "libft.h"

int	ft_strchr_bn(const char *s, int c)
{
	while (*s)
	{
		if (*s == (char)c)
			return (1);
		s++;
	}
	if ((char)c == 0 && *s == 0)
		return (0);
	return (0);
}
